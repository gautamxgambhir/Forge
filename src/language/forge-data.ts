// ── Fuzzy suggestion (Levenshtein distance 1) ──

export function suggestCommand(raw: string): string | null {
  const known = [
    "show", "inspect", "open", "search", "discover",
    "help", "clear", "hire", "future", "coffee", "matrix", "secret",
    "about", "profile", "skills", "projects", "contact", "resume",
    "timeline", "achievements", "stats", "experience", "socials",
  ];
  const lower = raw.toLowerCase();
  for (const kw of known) {
    if (levenshtein(lower, kw) <= 2) return kw;
  }
  return null;
}

function levenshtein(a: string, b: string): number {
  const m = a.length, n = b.length;
  const dp: number[][] = Array.from({ length: m + 1 }, (_, i) =>
    Array.from({ length: n + 1 }, (_, j) => (i === 0 ? j : j === 0 ? i : 0)),
  );
  for (let i = 1; i <= m; i++) {
    for (let j = 1; j <= n; j++) {
      dp[i]![j] = a[i - 1] === b[j - 1]
        ? dp[i - 1]![j - 1]!
        : 1 + Math.min(dp[i - 1]![j]!, dp[i]![j - 1]!, dp[i - 1]![j - 1]!);
    }
  }
  return dp[m]![n]!;
}
