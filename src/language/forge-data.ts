export function searchPortfolio(keyword: string): string {
  const kw = keyword.toLowerCase().trim();
  if (!kw) return '  Usage:  search <keyword>';

  const matches = SEARCH_CORPUS.filter((e) =>
    e.content.includes(kw) || e.label.toLowerCase().includes(kw) || e.command.includes(kw),
  );

  if (matches.length === 0) {
    return `  No results for "${keyword}".\n  Try: search python · search ai · search hacking`;
  }

  const lines = [
    `  Search results for "${keyword}"`,
    `  ${"─".repeat(38)}`,
  ];

  for (const m of matches) {
    lines.push(`  ${m.label.padEnd(24)}→  ${m.command}`);
  }

  lines.push(`\n  Run the suggested commands to explore.`);
  return lines.join("\n");
}

